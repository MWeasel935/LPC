import pygame


def create_arena():
    wall_list = []

    # Parse the level string above.
    x = y = 0
    for row in level:
        for col in row:
            if col == "1":
                wall_list.append(pygame.Rect(x, y, 20, 20))
            x += 20
        y += 20
        x = 0

    return wall_list


